p = 6277101735386680763835789423207666416083908700390324961279
F = GF(p)


a_range = range(-5, 6)        # chỉnh phạm vi a
b_range = range(-5, 6)        # chỉnh phạm vi b
x_candidates = [1, 2, 3, 5, 10]  # các x để thử làm điểm


for a in a_range:
    for b in b_range:

        try:
            E = EllipticCurve(F, [a, b])
        except Exception:
            continue
        if E.discriminant() == 0:
            continue

        for x0 in x_candidates:
            rhs = Integer(x0**3 + a*x0 + b) % p

            if rhs == 0:
                ys = [0]
            else:
                ls = kronecker(rhs, p)
                if ls == -1:
                    continue

                try:
                    pts = E.points(x0)   
                except Exception:
                    pts = []
                    for y in range(0, 1000):
                        if (y*y - rhs) % p == 0:
                            pts.append(E(x0, y))
            for P in pts:
                try:
                    ordP = P.order()
                except Exception:
                    continue
                # In định dạng: a b x0 order
                print(f"{a}\t{b}\t{x0}\t{ordP}")

